class Intermediario {
    private String nombre;
    private String telefono;
    private String agencia;
    private double costosPromocion;
    private double gananciaEsperada;
    private String fechaTrato;
    private boolean rentaConIntermediario;
    private double gananciaRenta;
    private double costoRenta;
    
    // Getters and Setters
    public String getNombre() {
        return nombre;
    }
    
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    
    public String getTelefono() {
        return telefono;
    }
    
    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }
    
    public String getAgencia() {
        return agencia;
    }
    
    public void setAgencia(String agencia) {
        this.agencia = agencia;
    }
    
    public double getCostosPromocion() {
        return costosPromocion;
    }
    
    public void setCostosPromocion(double costosPromocion) {
        this.costosPromocion = costosPromocion;
    }
    
    public double getGananciaEsperada() {
        return gananciaEsperada;
    }
    
    public void setGananciaEsperada(double gananciaEsperada) {
        this.gananciaEsperada = gananciaEsperada;
    }
    
    public String getFechaTrato() {
        return fechaTrato;
    }
    
    public void setFechaTrato(String fechaTrato) {
        this.fechaTrato = fechaTrato;
    }
    
    public boolean isRentaConIntermediario() {
        return rentaConIntermediario;
    }
    
    public void setRentaConIntermediario(boolean rentaConIntermediario) {
        this.rentaConIntermediario = rentaConIntermediario;
    }
    
    public double getGananciaRenta() {
        return gananciaRenta;
    }
    
    public void setGananciaRenta(double gananciaRenta) {
        this.gananciaRenta = gananciaRenta;
    }
    
    public double getCostoRenta() {
        return costoRenta;
    }
    
    public void setCostoRenta(double costoRenta) {
        this.costoRenta = costoRenta;
    }
}